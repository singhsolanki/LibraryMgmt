package com.yash.model;

public class Member {

	private int memberID;
	private String memberType;
	private String memberName;
	private String memberDepartment;
	private String memberClass;
	private String memberBranch;
	
	
	public int getMemberID() {
		return memberID;
	}
	public void setMemberID(int memberID) {
		this.memberID = memberID;
	}
	public String getMemberType() {
		return memberType;
	}
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberDepartment() {
		return memberDepartment;
	}
	public void setMemberDepartment(String memberDepartment) {
		this.memberDepartment = memberDepartment;
	}
	public String getMemberClass() {
		return memberClass;
	}
	public void setMemberClass(String memberClass) {
		this.memberClass = memberClass;
	}
	public String getMemberBranch() {
		return memberBranch;
	}
	public void setMemberBranch(String memberBranch) {
		this.memberBranch = memberBranch;
	}
	
	
	
}
